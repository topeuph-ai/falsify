"""Conformance tests for PRML v0.1 test vectors.

Each vector in spec/test-vectors/v0.1/test-vectors.json defines an input
manifest, the canonical UTF-8 bytes the canonicalizer MUST produce, and
the SHA-256 hex digest of those bytes.

If any of these fail, either:
  (a) the canonicalizer in falsify_prml.canonicalize has changed in a way
      that breaks the v0.1 contract — bump to v0.2 instead, OR
  (b) test vectors must be regenerated by running
      `python3 spec/test-vectors/v0.1/generate.py`.

Test vector contract is intentionally rigid. CI failure here is a
specification-level event, not a code-quality nit.
"""
from __future__ import annotations

import hashlib
import json
import sys
import unittest
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
VECTORS_PATH = REPO_ROOT / "spec" / "test-vectors" / "v0.1" / "test-vectors.json"

sys.path.insert(0, str(REPO_ROOT))
# The PRML conformance suite verifies the PRML manifest canonicalizer — the
# `falsify` command (falsify_prml), NOT the falsify-engine workflow canonicalizer
# (falsify.py), which canonicalizes a different schema and does not apply the
# PRML v0.1 threshold-float coercion.
import falsify_prml  # noqa: E402


def _load_vectors():
    if not VECTORS_PATH.exists():
        return None
    return json.loads(VECTORS_PATH.read_text(encoding="utf-8"))


VECTORS = _load_vectors() or []


class PRMLVectorTests(unittest.TestCase):
    """Generated dynamically: one (canonical, hash) pair of assertions per vector."""

    @classmethod
    def setUpClass(cls):
        if not VECTORS:
            raise unittest.SkipTest(f"test vectors not generated yet: {VECTORS_PATH}")

    def test_vector_count(self):
        """v0.1 ships 13 test vectors (TV-001..TV-013). TV-013 locks the
        integer-threshold->float coercion; it is a non-breaking, clarifying
        addition that leaves every prior vector's hash unchanged."""
        self.assertEqual(len(VECTORS), 13, f"Expected 13 vectors, got {len(VECTORS)}")

    def test_key_order_invariance(self):
        """TV-001 and TV-002 differ only in input key insertion order. Hashes MUST match."""
        by_id = {v["id"]: v for v in VECTORS}
        self.assertIn("TV-001", by_id)
        self.assertIn("TV-002", by_id)
        self.assertEqual(
            by_id["TV-001"]["hash"],
            by_id["TV-002"]["hash"],
            "Key-ordering invariance broken — TV-001 and TV-002 must produce identical hashes.",
        )

    def test_content_sensitivity(self):
        """TV-001 and TV-003 differ only by threshold (0.85 vs 0.86). Hashes MUST differ."""
        by_id = {v["id"]: v for v in VECTORS}
        self.assertIn("TV-001", by_id)
        self.assertIn("TV-003", by_id)
        self.assertNotEqual(
            by_id["TV-001"]["hash"],
            by_id["TV-003"]["hash"],
            "Content sensitivity broken — TV-001 and TV-003 produced identical hashes.",
        )

    def test_amendment_chain_linkage(self):
        """TV-009 amends TV-001; its prior_hash MUST equal TV-001's hash."""
        by_id = {v["id"]: v for v in VECTORS}
        self.assertEqual(
            by_id["TV-009"]["input"]["prior_hash"],
            by_id["TV-001"]["hash"],
            "Amendment chain broken — TV-009.prior_hash must equal TV-001.hash.",
        )


def _make_canonical_test(vector):
    def test(self):
        produced = falsify_prml.canonicalize(vector["input"])
        self.assertEqual(
            produced,
            vector["canonical"],
            f"{vector['id']} canonical bytes diverged from spec.\n"
            f"  Expected length: {len(vector['canonical'])} chars\n"
            f"  Produced length: {len(produced)} chars\n"
            f"  Either fix _canonicalize, or regenerate vectors and bump spec version.",
        )

    test.__doc__ = f"{vector['id']}: canonical bytes match spec"
    return test


def _make_hash_test(vector):
    def test(self):
        canonical = falsify_prml.canonicalize(vector["input"])
        produced_hash = hashlib.sha256(canonical.encode("utf-8")).hexdigest()
        self.assertEqual(
            produced_hash,
            vector["hash"],
            f"{vector['id']} hash mismatch.\n"
            f"  Expected: {vector['hash']}\n"
            f"  Produced: {produced_hash}\n"
            f"  This is a v0.1 conformance failure.",
        )

    test.__doc__ = f"{vector['id']}: SHA-256 of canonical bytes matches spec"
    return test


# Dynamically attach one canonical-bytes test and one hash test per vector
# so the unittest report shows each TV-XXX as a separate line.
for _v in VECTORS:
    _vid = _v["id"].replace("-", "_").lower()
    setattr(
        PRMLVectorTests,
        f"test_canonical_bytes_{_vid}",
        _make_canonical_test(_v),
    )
    setattr(
        PRMLVectorTests,
        f"test_hash_{_vid}",
        _make_hash_test(_v),
    )


if __name__ == "__main__":
    unittest.main()
